# Definindo duas variavei para comparação 
a = 5
b = 10

# Usando opradores de comparação 
if a == b: 
    print("a é igual a b")
elif a != b: 
    print("a é diferente de b")
if a < b:
    print("a é menor que b")
elif a > b:
    print("a é maior que b")

if a <= 5:
    print("a é menor ou igual a 5")
if b >= 10:
    print("b é maior ou igual a 10")